#Operators are special symbols used to perform operations on variables and values to manipulate data.

num1 = 10
num2 = 2

#Arithmetic Operators
print("num1 + num2 is", num1 + num2)
print("num1 - num2 is", num1 - num2)
print("num1 * num2 is", num1 * num2)
print("num1 / num2 is", num1 / num2)
print("num1 % num2 is", num1 % num2)
print("num1 // num2 is", num1 // num2) #floor 
print("num1 ** num2 is", num1 ** num2) #power

#Arithmetic Operators
a = 4
print(a) #4

a += 4
print(a) #8

a -= 4  
print(a) #4

a *= 4
print(a) #16

a /= 4
print(a) #4.0

a //= 4
print(a) #1.0 

a **= 4
print(a) #1.0

#Comparison Operators
x = 8
y = 3
z = 8
print(x > y) #True
print(x < y) #False
print(x != y) #True
print(x == z) #True

#Logical Operators
print(x > y and x == z) #True
print(x > y or x > z) #True
print(not(x > y)) #False


